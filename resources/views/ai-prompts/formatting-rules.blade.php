IMPORTANT FORMATTING GUIDELINES:

STRUCTURE:
- Start with a 2-3 sentence engaging hook paragraph (NO heading at the start)
- Use ## for main section headings
- Use ### for subsection headings
- End with a clear conclusion and call to action

PARAGRAPHS:
- Keep paragraphs to 3-5 sentences maximum
- Add blank lines between ALL paragraphs and sections
- Use clear, simple language appropriate for all audiences
- Each paragraph should focus on one main idea

LISTS:
- Use bullet points (-) for lists, not numbered lists
- Add space between list items for better readability
- Keep list items concise (1-2 sentences)

EMPHASIS & STYLE:
- Bold important theological terms using **term**
- Use > for Bible quotes and key spiritual truths
- Cite scripture references in parentheses: (John 3:16)
- Use italics sparingly for emphasis: *word*

HEADINGS:
- Use descriptive, engaging heading titles
- Add generous spacing above and below headings
- Maintain consistent heading hierarchy

EXAMPLE STRUCTURE:
```
[Opening hook paragraph - 2-3 sentences, no heading]

## Introduction
[Why this topic matters - 1-2 paragraphs]

## Key Bible Verses
[List of primary scriptures with brief context]

## Biblical Teaching
[Main content with subsections using ###]

### The Father's Love
[Content here]

### The Son's Mission
[Content here]

## Practical Application
[How to apply these truths today]

## Discussion Questions
- [Thought-provoking question 1]
- [Thought-provoking question 2]
- [Thought-provoking question 3]

## Conclusion
[Summary and call to action]
```

READABILITY TIPS:
- Shorter is better - break up long paragraphs
- Use transition words between sections
- Vary sentence length for natural flow
- End sections with impact
